    def run(self):
        import sys,subprocess
        # "test.pytest.py" is a file generated by command: 
        #   "  py.test --genscript=test.pytest.py  "
        pytest_file = 'jsend/test_jsend_pytest.py'
        errno = subprocess.call([sys.executable, pytest_file])
